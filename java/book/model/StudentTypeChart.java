package book.model;

public class StudentTypeChart{
	
	public String stLabel;
	public int stNum;
	
	public StudentTypeChart() {
		super();
	}
	public String getStLabel() {
		return stLabel;
	}
	public void setStLabel(String stLabel) {
		this.stLabel = stLabel;
	}
	public int getStNum() {
		return stNum;
	}
	public void setStNum(int stNum) {
		this.stNum = stNum;
	}
	
	
	
}